package name.paulevans.txnidgenerator.common;

/**
 * <p>Simple wrapper class that wraps a transaction identifier string
 * value.</p>
 * 
 * @author Paul R Evans
 *
 */
public class TxnIdentifier {

	/**
	 * The transaction identifier value
	 */
	private final String _identifierValue;
	
	/**
	 * <p>Default constructor - the identifier value returned by 
	 * {@link #getIdentifierValue()} will be null.</p>
	 */
	public TxnIdentifier() {
		_identifierValue = null;
	}
	
	/**
	 * <p>Constructs a new instance with the transaction identifier value
	 * initialized to the specified input.</p>
	 * 
	 * @param pIdentifierValue the transaction identifier value to use
	 */
	public TxnIdentifier(final String pIdentifierValue) {
		_identifierValue = pIdentifierValue;
	}
	
	/**
	 * Returns the transaction identifier value
	 * 
	 * @return the transaction identifier value
	 */
	public final String getIdentifierValue() {
		return _identifierValue;
	}
}
