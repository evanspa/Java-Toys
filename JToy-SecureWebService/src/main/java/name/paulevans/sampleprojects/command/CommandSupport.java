package name.paulevans.sampleprojects.command;

import name.paulevans.sampleprojects.model.Order;

/**
 * <p>Convenience class for concrete commands to extend from.</p>
 * 
 * @author Paul R Evans
 *
 */
public abstract class CommandSupport implements Command {

	/**
	 * Order instance
	 */
	private Order order;

	/**
	 * @param pOrder the order to set
	 */
	public void setOrder(Order pOrder) {
		this.order = pOrder;
	}

	/**
	 * @return the order
	 */
	public Order getOrder() {
		return order;
	}

}
